package com.itrustmachines.bnsautofolderattest.exception;

public class InputException extends BnsAutoFolderAttestException {
  
  public InputException() {
    super();
  }
  
  public InputException(String msg) {
    super(msg);
  }
  
  public InputException(String message, Throwable cause) {
    super(message, cause);
  }
  
  public InputException(Throwable cause) {
    super(cause);
  }
  
}
