package com.itrustmachines.bnsautofolderattest.exception;

public class CallbackException extends BnsAutoFolderAttestException {
  
  public CallbackException() {
    super();
  }
  
  public CallbackException(String msg) {
    super(msg);
  }
  
  public CallbackException(String message, Throwable cause) {
    super(message, cause);
  }
  
  public CallbackException(Throwable cause) {
    super(cause);
  }
  
}
